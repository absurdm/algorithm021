/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func preorderTraversal(root *TreeNode) []int {
	var res []int
	var inorder func(node *TreeNode)
	inorder = func(node *TreeNode) {
		// 遍历中止条件子节点不存在, 返回上一层调用栈
		if node == nil {
			return
		}
		// 中
		res = append(res, node.Val)
		// 左
		inorder(node.Left)
		// 右
		inorder(node.Right)
	}
	inorder(root)
	return res
}
